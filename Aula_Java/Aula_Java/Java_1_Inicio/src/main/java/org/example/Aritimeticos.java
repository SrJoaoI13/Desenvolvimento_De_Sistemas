package org.example;

public class Aritimeticos {
    public static void main(String[] args) {
        // Declarações de variaveis
        int a = 10;
        int b = 2;

        //operadores aritimeticos
        int soma = a + b;
        int mutiplicador = a * b;
        int subtrair  = a - b;
        int divisor = a / b;

        // exibir resultado
        System.out.println("Resultado da soma: " +soma);
        System.out.println("Resultado da mutiplicador: " +mutiplicador);
        System.out.println("Resultado da subtrair: " +subtrair);
        System.out.println("Resultado da divisor: " +divisor);

    }
}
