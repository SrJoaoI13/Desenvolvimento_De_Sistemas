// criando  um vetor 

const listaNomes = ['Marta','José','Maria']

console.log("\nExibindo todos os elementos: ");
console.log(listaNomes);

console.log("\nExibindo primeiro elemento: ");
console.log(listaNomes[0]);

console.log("\nAdicionando mais um elemento no vetor: ");
listaNomes.push('João');
console.log(listaNomes);

console.log("\nRemovendo o primeiro elemento do vetor: ");
listaNomes.shift;
console.log(listaNomes);

console.log("\nRemovendo o ultimo nome: ");
listaNomes.pop();
console.log(listaNomes);